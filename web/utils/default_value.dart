class DefaultValue {
  bool? defaultString;
  bool? defaultInt;
  bool? defaultDouble;
  bool? defaultBool;
  bool? defaultList;
  String boolValue;

  DefaultValue({
    this.defaultString = false,
    this.defaultInt = false,
    this.defaultDouble = false,
    this.defaultBool = false,
    this.defaultList = false,
    required this.boolValue,
  });
}
